#!/usr/bin/env node

// Capture Merkle test vectors from the TS impl and write them to
// schemas/merkle-test-vectors.json. This file is the cross-language
// covenant — both core/__tests__/merkle.test.ts and
// packages/sdk-py/tests/test_merkle_parity.py read it. Regenerate ONLY
// when you've intentionally changed the Merkle impl; drift shows up
// immediately in both suites.
//
// Why a separate capture step rather than computing on the fly in
// tests: committing the exact bytes makes the Python verifier a true
// parity check (Python can't reconstruct TS-impl output from scratch
// — it can only prove it accepts the TS-committed bytes). If the
// numbers ever disagree, both suites fail and the root cause is the
// impl delta, not an accidental drift of the test itself.

import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(SCRIPT_DIR, "..", "..");

// Import the built TS directly — core/package.json has main: src/index.ts,
// and tsx/vitest handle the .ts → .js runtime dance. For a plain node
// script we use the already-compiled sibling .js entries when present,
// or fall back to evaluating via the ts-runtime. Simplest path: the
// whole TS module is just functions + SHA-256; rewrite tiny in JS.
import { sha256 } from "@noble/hashes/sha2.js";

const EMPTY_TREE_ROOT = sha256(new Uint8Array(0));
const textEncoder = new TextEncoder();

function hex(bytes) {
  return Array.from(bytes)
    .map((b) => b.toString(16).padStart(2, "0"))
    .join("");
}

function bytes(s) {
  return textEncoder.encode(s);
}

function leafHash(preimage) {
  const buf = new Uint8Array(1 + preimage.length);
  buf[0] = 0x00;
  buf.set(preimage, 1);
  return sha256(buf);
}

function interiorHash(left, right) {
  const buf = new Uint8Array(65);
  buf[0] = 0x01;
  buf.set(left, 1);
  buf.set(right, 33);
  return sha256(buf);
}

function largestPow2LessThan(n) {
  let k = 1;
  while (k * 2 < n) k *= 2;
  return k;
}

function mth(leaves) {
  if (leaves.length === 0) return EMPTY_TREE_ROOT;
  if (leaves.length === 1) return leafHash(leaves[0]);
  const k = largestPow2LessThan(leaves.length);
  return interiorHash(mth(leaves.slice(0, k)), mth(leaves.slice(k)));
}

function inclusionProof(leaves, m) {
  return pathRec(m, leaves);
}

function pathRec(m, d) {
  if (d.length === 1) return [];
  const k = largestPow2LessThan(d.length);
  if (m < k) return [...pathRec(m, d.slice(0, k)), mth(d.slice(k))];
  return [...pathRec(m - k, d.slice(k)), mth(d.slice(0, k))];
}

function consistencyProof(leaves, m) {
  const n = leaves.length;
  if (m === 0 || m === n) return [];
  return subProof(m, leaves, true);
}

function subProof(m, d, b) {
  const n = d.length;
  if (m === n) return b ? [] : [mth(d)];
  const k = largestPow2LessThan(n);
  if (m <= k) return [...subProof(m, d.slice(0, k), b), mth(d.slice(k))];
  return [...subProof(m - k, d.slice(k), false), mth(d.slice(0, k))];
}

// Canonical leaf set: ["0", "1", ..., "N-1"] as UTF-8.
const MAX_N = 8;
const allLeaves = Array.from({ length: MAX_N }, (_, i) => bytes(String(i)));
const allStrings = Array.from({ length: MAX_N }, (_, i) => String(i));

const inclusion = [];
for (let n = 1; n <= MAX_N; n++) {
  const leaves = allLeaves.slice(0, n);
  const leafStrings = allStrings.slice(0, n);
  const root = mth(leaves);
  for (let i = 0; i < n; i++) {
    inclusion.push({
      leaves: leafStrings,
      leaf_index: i,
      proof_hex: inclusionProof(leaves, i).map(hex),
      root_hex: hex(root),
    });
  }
}

const consistency = [];
for (let n = 0; n <= MAX_N; n++) {
  const leaves = allLeaves.slice(0, n);
  const leafStrings = allStrings.slice(0, n);
  const newRoot = mth(leaves);
  for (let m = 0; m <= n; m++) {
    const oldRoot = mth(leaves.slice(0, m));
    consistency.push({
      leaves: leafStrings,
      m,
      n,
      proof_hex: consistencyProof(leaves, m).map(hex),
      old_root_hex: hex(oldRoot),
      new_root_hex: hex(newRoot),
    });
  }
}

const out = {
  $comment:
    "Cross-language Merkle test vectors. Generated by scripts/capture-merkle-vectors.mjs and consumed by both TS (core/__tests__/merkle.test.ts) and Python (packages/sdk-py/tests/test_merkle_parity.py) test suites. Root hashes follow RFC 9162 §2.1 (0x00 prefix for leaves, 0x01 for interior nodes). Leaves are the UTF-8 encodings of the strings in `leaves`. To regenerate after an impl change: pnpm capture:merkle-vectors — but note that this RE-PINS the cross-language covenant, so every verifier (TS + Python + future Go daemon) must agree on the new output.",
  empty_tree_root_hex: hex(EMPTY_TREE_ROOT),
  leaf_empty_input_hex: hex(leafHash(new Uint8Array())),
  inclusion,
  consistency,
};

const VECTORS_PATH = join(REPO_ROOT, "schemas", "merkle-test-vectors.json");
writeFileSync(VECTORS_PATH, `${JSON.stringify(out, null, 2)}\n`);
console.log(
  `✓ captured ${inclusion.length} inclusion + ${consistency.length} consistency vectors → schemas/merkle-test-vectors.json`,
);
